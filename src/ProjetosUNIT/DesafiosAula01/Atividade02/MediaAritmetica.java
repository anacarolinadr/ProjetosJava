package ProjetosUNIT.DesafiosAula01.Atividade02;

public class MediaAritmetica {

    public double media987() {
        return (9 + 8 + 7) / 3;
    }

    public double media654() { return (6 + 5 + 4) / 3; }

    public double mediaDasMedias() {
        return (media987() + media654()) / 2;
    }
}
